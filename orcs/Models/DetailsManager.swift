        case "Цистерни з ППМ": return
"""

"""
        case "Засоби ППО": return
"""

"""
        case "Спецтехніка": return
"""

"""
        case "Установки ОТРК": return
"""

"""
        case "Автотехніка": return
"""

"""
        case "Крилаті Ракети": return
"""

"""
        default: return "Якась неіснуюча техніка"
        }
    }
    
}
